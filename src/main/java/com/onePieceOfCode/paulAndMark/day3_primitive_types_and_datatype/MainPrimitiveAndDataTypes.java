package com.onePieceOfCode.paulAndMark.day3_primitive_types_and_datatype;

public class MainPrimitiveAndDataTypes {
    public static void main(String[] args) {

        //8 bit
        byte yearsOfService =  5;
        System.out.println(yearsOfService);

        short departmentCode = 101;
        System.out.println(departmentCode);

        //32 bit
        int age = 23;
        System.out.println(age);

        //64 bit
        long empId = 1234567890987L;
        System.out.println(empId);

        //64 bit
        double salary = 50000;
        System.out.println(salary);

        //32 bit
        float hourlyRate = 950.75f;
        System.out.println(hourlyRate);

        boolean isRegularEmployee = true;
        System.out.println(isRegularEmployee);

        char performance = 'b';
        System.out.println(performance);

        String name = "paul";
        System.out.println(name);






    }
}
